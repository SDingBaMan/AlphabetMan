public class userDataDay {
    private String id;
    private String datetime;
    private String YunNum;
    private String lastYanNumber;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDatetime() {
        return datetime;
    }

    public void setDatetime(String datetime) {
        this.datetime = datetime;
    }

    public String getYunNum() {
        return YunNum;
    }

    public void setYunNum(String yunNum) {
        YunNum = yunNum;
    }

    public String getLastYanNumber() {
        return lastYanNumber;
    }

    public void setLastYanNumber(String lastYanNumber) {
        this.lastYanNumber = lastYanNumber;
    }
}
